#include "calculadora.h"
#include <iostream>

int somar(int a, int b, int* result) {
    *result = a + b;
}

int subtrair(int a, int b, int* result) {
    *result = a - b;
}

int multiplicar(int a, int b, int* result) {
    *result = a * b;
}































